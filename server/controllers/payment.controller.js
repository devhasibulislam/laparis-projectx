/* internal import */
const paymentService = require("../services/payment.service");

/* create an order */
exports.createOrder = async (req, res) => {
  try {
    await paymentService.createOrder(req, res);
  } catch (error) {
    next(error);
  } finally {
    console.log(`Route: ${req.url} || Method: ${req.method}`);
  }
};

/* verify an order */
exports.verifyOrder = async (req, res, next) => {
  try {
    await paymentService.verifyOrder(req, res);
  } catch (error) {
    next(error);
  } finally {
    console.log(`Route: ${req.url} || Method: ${req.method}`);
  }
};
